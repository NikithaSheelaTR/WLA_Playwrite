namespace Framework.Common.UI.Products.WestlawEdgePremium.Enums.LitigationAnalytics
{
    /// <summary>
    /// Specifies the columns that can be displayed in the Opportunity Finder.
    /// </summary>
    public enum OpportunityFinderColumnsToDisplay
    {
        /// <summary>
        /// Select all columns.
        /// </summary>
        SelectAll,

        /// <summary>
        /// The name of the company.
        /// </summary>
        CompanyName,

        /// <summary>
        /// The street address of the company.
        /// </summary>
        StreetAddress,

        /// <summary>
        /// The city where the company is located.
        /// </summary>
        City,

        /// <summary>
        /// The state where the company is located.
        /// </summary>
        State,

        /// <summary>
        /// The country where the company is located.
        /// </summary>
        Country,

        /// <summary>
        /// The zip code of the company's address.
        /// </summary>
        ZipCode,

        /// <summary>
        /// The website URL of the company.
        /// </summary>
        WebsiteUrl,

        /// <summary>
        /// The telephone number of the company.
        /// </summary>
        TelephoneNumber,

        /// <summary>
        /// The NAICS (North American Industry Classification System) code of the company.
        /// </summary>
        NAICSCode,

        /// <summary>
        /// The SIC (Standard Industrial Classification) code of the company.
        /// </summary>
        SICCode,

        /// <summary>
        /// The count of litigation events associated with the company.
        /// </summary>
        LitigationEventCount,

        /// <summary>
        /// The number of employees in the company.
        /// </summary>
        NumberOfEmployees,

        /// <summary>
        /// The revenue of the company.
        /// </summary>
        Revenue,

        /// <summary>
        /// The authority ID associated with the company.
        /// </summary>
        AuthorityID,

        /// <summary>
        /// The ticker symbol of the company.
        /// </summary>
        TickerSymbol
    }
}