namespace Framework.Common.UI.Products.WestlawEdge.Models.QuickCheck
{
    /// <summary>
    /// The best citation model.
    /// </summary>
    public class BestCitationModel
    {
        /// <summary>
        /// Gets or sets the file name.
        /// </summary>
        public string FileName { get; set; }

        /// <summary>
        /// Gets or sets the quotes.
        /// </summary>
        public string Quotes { get; set; }

        /// <summary>
        /// Gets or sets the guid.
        /// </summary>
        public string Guid { get; set; }

        /// <summary>
        /// Gets or sets the citation.
        /// </summary>
        public string Citation { get; set; }

        /// <summary>
        /// Gets or sets the number of words.
        /// </summary>
        public string NumberOfWords { get; set; }

        /// <summary>
        /// Gets or sets the block spanning.
        /// </summary>
        public string BlockSpanning { get; set; }

        /// <summary>
        /// Gets or sets the bracket ellipse.
        /// </summary>
        public string BracketEllipse { get; set; }
    }
}