namespace Framework.Common.UI.Products.Shared.Enums.SortingTypes
{
    /// <summary>
    /// The sort order.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// The sequence is sorted from smallest to largest. For example, A to Z.
        /// </summary>
        Ascending,

        /// <summary>
        /// The sequence is sorted from largest to smallest. For example, Z to A.
        /// </summary>
        Descending,

        /// <summary>
        /// The sequence is unsorted
        /// </summary>
        Unsorted,

        /// <summary>
        /// None
        /// </summary>
        None
    }
}